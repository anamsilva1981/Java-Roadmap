package baseJava;

public class VariaveisNotasEstudo {
    public static void main(String[] args) {
        int dia = 29;
        int mes = 12;
        int ano = 2024;

        // Atrbuir novo valor a variavel
        ano = 2025;

        System.out.println(dia);
        System.out.println(mes);
        System.out.println(ano);
    }
}